let fruits = prompt("Enter Your Fruit Name");
console.log(fruits);

switch (fruits) {
  case "orange":
    console.log("This is orange");
    break;
  case "apple":
    console.log("This is apple");
    break;
  case "mango":
    console.log("This is mango");
    break;
  case "watermelon":
    console.log("This is watermelon");
    break;
  default:
    console.log("empty");
}
